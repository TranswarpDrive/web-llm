#!/usr/bin/env node
import { pbkdf2Sync, randomBytes } from 'node:crypto';
import { exit, stdin, stdout } from 'node:process';

const ITERATIONS = 100000;
const KEY_LENGTH = 32;
const DIGEST = 'sha256';
const MIN_PASSWORD_LENGTH = 12;

function hiddenQuestion(prompt) {
  if (!stdin.isTTY) {
    throw new Error('No interactive TTY. Set ADMIN_PASSWORD for non-interactive use.');
  }

  return new Promise(resolve => {
    let value = '';

    function cleanup() {
      stdin.setRawMode(false);
      stdin.pause();
      stdin.off('data', onData);
      stdout.write('\n');
    }

    function onData(chunk) {
      const input = String(chunk);

      if (input === '\u0003') {
        cleanup();
        exit(130);
      }

      if (input === '\r' || input === '\n') {
        cleanup();
        resolve(value);
        return;
      }

      if (input === '\u007f') {
        if (value.length > 0) {
          value = value.slice(0, -1);
          stdout.write('\b \b');
        }
        return;
      }

      value += input;
      stdout.write('*');
    }

    stdout.write(prompt);
    stdin.setEncoding('utf8');
    stdin.setRawMode(true);
    stdin.resume();
    stdin.on('data', onData);
  });
}

async function readPassword() {
  if (process.env.ADMIN_PASSWORD) return process.env.ADMIN_PASSWORD;

  const password = await hiddenQuestion('Admin password: ');
  const confirm = await hiddenQuestion('Confirm admin password: ');
  if (password !== confirm) {
    throw new Error('Passwords do not match.');
  }
  return password;
}

function sqlString(value) {
  return value.replaceAll("'", "''");
}

try {
  const password = await readPassword();
  if (password.length < MIN_PASSWORD_LENGTH) {
    throw new Error(`Password must be at least ${MIN_PASSWORD_LENGTH} characters.`);
  }

  const salt = randomBytes(32);
  const hash = pbkdf2Sync(password, salt, ITERATIONS, KEY_LENGTH, DIGEST);
  const hashBase64 = hash.toString('base64');
  const saltBase64 = salt.toString('base64');

  stdout.write(`-- Generated by npm run hash-password
-- Paste this into the Supabase SQL Editor after running the schema.
INSERT INTO users (username, display_name, password_hash, password_salt)
VALUES ('admin', 'Admin', '${sqlString(hashBase64)}', '${sqlString(saltBase64)}')
ON CONFLICT (username) DO UPDATE
SET password_hash = EXCLUDED.password_hash,
    password_salt = EXCLUDED.password_salt,
    updated_at = now();
`);
} catch (error) {
  console.error(error instanceof Error ? error.message : error);
  exit(1);
}
